    public void PruebaJComboBox(){
		// Creacion del JTextField
		jTextField1 = new JTextField(20);
		
		// Creacion del JComboBox y aÃ±adir los items.
		
		cBT.addItem("uno");
		cBT.addItem("dos");
		cBT.addItem("tres");
		
		// Accion a realizar cuando el JComboBox cambia de item seleccionado.
		cBT.addActionListener((ActionEvent e) -> {
                    jTextField1.setText(cBT.getSelectedItem().toString());
                    this.lbl.setText(cBT.getSelectedItem().toString());
                });

		// Creacion de la ventana con los componentes

		this.getContentPane().setLayout(new FlowLayout());
		this.getContentPane().add(cBT);
		this.getContentPane().add(jTextField1);
		this.pack();
		this.setVisible(true);
		this.setDefaultCloseOperation(WindowConstants.EXIT_ON_CLOSE);
	}
